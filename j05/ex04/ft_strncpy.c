char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int		i;

	i = 0;
	if (n > 0)
	{
		while ((src[i] != '\0') && (i <= n - 1))
		{
			dest[i] = src[i];
			i++;
		}
		if (i < n)
		{
			dest[i] = '\0';
			i++;
		}
	}
	return (dest);
}
