/*
** Print numbers as char from 0 to 9
** '0' != 0, '1' != 1, ..., '9' != 9
*/

int		ft_putchar(char c);

void	ft_print_numbers(void)
{
	char	ch;

	ch = '0';
	while (ch <= '9')
	{
		ft_putchar(ch);
		ch = ch + 1;
	}
}
